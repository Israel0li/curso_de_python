# conversão de tipos, coeção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# # str, int, float, bool

# str:
print('a' + 'b') # resultado = 'ab'

# int:
print(int('1'), type(int('1')))

# float:
print(type(float('1.0') + 1))

# bool:
print(10 == 10) # true
print(10 == -10) # false

